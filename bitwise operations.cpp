#include <bits/stdc++.h>
using namespace std;

int main() {
    int a = 0x01, b = 0x02;
 
    cout<<"a = " << a <<","<< " b = " << b <<endl;
    cout << "a & b = " << (a & b) << endl;
 
    cout << "a | b = " << (a | b) << endl;

    cout<<"b << 1" <<" = "<< (b << 1) <<endl;

    cout<<"b >> 1 "<<"= " << (b >> 1 )<<endl;
 
    return 0;

}
